export function buildChildEnv(source, allowedKeys, envExtra, forbiddenKeys) {
  const env = {};
  for (const key of [...BASE_CHILD_KEYS, ...allowedKeys]) {
    if (source[key] !== undefined) env[key] = source[key];
  }
  Object.assign(env, envExtra, { PORT: String(envExtra.PORT) });
  for (const key of forbiddenKeys) {
    if (env[key] !== undefined) throw new Error(`forbidden child environment key: ${key}`);
  }
  return env;
}
